#include "3-calc.h"

/**
 * op_add - add two int
 * @a: first int
 * @b: second int
 * Return: sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two int
 * @a: first int
 * @b: second int
 * Return: sub
*/

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply two int
 * @a: first int
 * @b: second int
 * Return: multiplication
*/

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide two int
 * @a: first int
 * @b: second int
 * Return: division of a / b
*/

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - reminder of an integer
 * @a: first int
 * @b: second int
 * Return: reminder a % b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
